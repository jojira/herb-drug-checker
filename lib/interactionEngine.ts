// Main engine function
// ---------------------------------------------------------------------------

/**
 * Runs a full herb-drug interaction check.
 *
 * @param westernMeds - Array of western medications
 * @param tcmInput    - Single herb or TCM formula
 * @param options     - excludedHerbIds: array of herb IDs to exclude from check
 */
export function checkInteractions(
  westernMeds: WesternMed[],
  tcmInput: TCMInput,
  options: CheckInteractionsOptions = {}
): InteractionEngineResult {
  const matches: InteractionMatch[] = [];
  const checkedWithNoInteraction: string[] = [];
  let formulaBreakdown: InteractionEngineResult["formulaBreakdown"] | undefined;

  const excludedSet = new Set(options.excludedHerbIds ?? []);

  // ── Step 1: Resolve herb IDs to check ─────────────────────────────────────
  let herbIdsToCheck: string[];
  let allFormulaHerbIds: string[] = [];
  let resolvedFormula: FormulaIdentity | undefined;

  if (tcmInput.type === "herb") {
    herbIdsToCheck = [tcmInput.herbId];
  } else {
    const resolved = resolveFormula(tcmInput.formulaId);
    if (!resolved) {
      return {
        worstSeverity: "none",
        worstSeverityColor: "green",
        matches: [],
        unresolvedHerbs: [],
        hasUnresolvedHerbs: false,
        checkedWithNoInteraction: [],
        checkedAt: new Date().toISOString(),
        disclaimer: DISCLAIMER,
        dataStatus: "mock_unverified",
      };
    }
    resolvedFormula = resolved.formula;
    allFormulaHerbIds = resolved.herbIds;
    herbIdsToCheck = allFormulaHerbIds.filter((id) => !excludedSet.has(id));
  }

  // ── Step 2: Cross-reference each herb against each western med ─────────────
  const herbInteractionMap: Map<string, InteractionMatch[]> = new Map();

  for (const herbId of herbIdsToCheck) {
    // Unresolved herbs have no interaction data — skip lookup
    if (herbId.startsWith("unresolved-")) {
      herbInteractionMap.set(herbId, []);
      continue;
    }

    const herbMatches: InteractionMatch[] = [];

    for (const med of westernMeds) {
      const match = lookupInteraction(herbId, med.rxcui);
      if (match) {
        const enriched: InteractionMatch = {
          ...match,
          fromFormula: tcmInput.type === "formula",
          sourceFormula: resolvedFormula,
        };
        herbMatches.push(enriched);
        matches.push(enriched);
      }
    }

    if (herbMatches.length === 0) {
      checkedWithNoInteraction.push(herbId);
    }

    herbInteractionMap.set(herbId, herbMatches);
  }

  // ── Step 3: Sort matches — worst severity first, then by evidence level ───
  const evidenceRank: Record<EvidenceLevel, number> = { high: 2, medium: 1, low: 0 };
  matches.sort((a, b) => {
    const severityDiff = SEVERITY_RANK[b.severity] - SEVERITY_RANK[a.severity];
    if (severityDiff !== 0) return severityDiff;
    return evidenceRank[b.evidenceLevel] - evidenceRank[a.evidenceLevel];
  });

  // ── Step 4: Determine worst overall severity ───────────────────────────────
  const worstSeverity = matches.reduce<SeverityLevel>(
    (worst, m) => escalateSeverity(worst, m.severity),
    "none"
  );

  // ── Step 5: Build formula breakdown ───────────────────────────────────────
  const unresolvedHerbs: FormulaHerbDetail[] = [];

  if (tcmInput.type === "formula" && resolvedFormula) {
    const herbs: FormulaHerbDetail[] = allFormulaHerbIds.map((herbId) => {
      const isExcluded = excludedSet.has(herbId);
      const isUnresolved = herbId.startsWith("unresolved-");
      const libraryHerb = lookupHerbById(herbId);

      if (isUnresolved) {
        const detail: FormulaHerbDetail = {
          herbId,
          pinyin: libraryHerb?.tcmbank_name ?? herbId.replace("unresolved-", ""),
          latin: "",
          english: "Unresolved Herb",
          nccaom_code: null,
          trustTier: "unresolved",
          resolved: false,
          excluded: isExcluded,
          hasInteraction: false,
          interactions: [],
        };
        return detail;
      }

      if (isExcluded) {
        return {
          herbId,
          pinyin:      libraryHerb?.pinyin      ?? herbId,
          latin:       libraryHerb?.latin       ?? "",
          english:     libraryHerb?.english     ?? "",
          nccaom_code: libraryHerb?.nccaom_code ?? null,
          trustTier:   (libraryHerb?.trustTier  ?? "unresolved") as FormulaHerbDetail["trustTier"],
          resolved:    libraryHerb?.resolved    ?? false,
          excluded: true,
          hasInteraction: false,
          interactions: [],
          taste:       libraryHerb?.taste,
          temperature: libraryHerb?.temperature,
          channels:    libraryHerb?.channels,
          properties:  libraryHerb?.properties,
          tcm_cautions: libraryHerb?.tcm_cautions,
        };
      }

      const herbMatches = herbInteractionMap.get(herbId) ?? [];

      return {
        herbId,
        pinyin:      libraryHerb?.pinyin      ?? herbId,
        latin:       libraryHerb?.latin       ?? "",
        english:     libraryHerb?.english     ?? "",
        nccaom_code: libraryHerb?.nccaom_code ?? null,
        trustTier:   (libraryHerb?.trustTier  ?? "unresolved") as FormulaHerbDetail["trustTier"],
        resolved:    libraryHerb?.resolved    ?? false,
        excluded: false,
        hasInteraction: herbMatches.length > 0,
        interactions: herbMatches,
        taste:       libraryHerb?.taste,
        temperature: libraryHerb?.temperature,
        channels:    libraryHerb?.channels,
        properties:  libraryHerb?.properties,
        tcm_cautions: libraryHerb?.tcm_cautions,
      };
    });

    // Collect unresolved herbs for top-level result field
    for (const h of herbs) {
      if (h.trustTier === "unresolved" && !h.excluded) {
        unresolvedHerbs.push(h);
      }
    }

    const flaggedHerbCount = herbs.filter((h) => h.hasInteraction && !h.excluded).length;
    const unresolvedCount = herbs.filter((h) => h.trustTier === "unresolved").length;

    formulaBreakdown = {
      formula: resolvedFormula,
      herbs,
      flaggedHerbCount,
      totalHerbCount: herbs.length,
      unresolvedCount,
    };
  }

  return {
    worstSeverity,
    worstSeverityColor: severityToColor(worstSeverity),
    matches,
    unresolvedHerbs,
    hasUnresolvedHerbs: unresolvedHerbs.length > 0,
    formulaBreakdown,
    checkedWithNoInteraction,
    checkedAt: new Date().toISOString(),
    disclaimer: DISCLAIMER,
    dataStatus: "mock_unverified",
  };
}
