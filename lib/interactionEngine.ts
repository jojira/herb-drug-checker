/**
 * Resolves a formula ID to its constituent herb IDs.
 * Returns null if the formula is not found.
 */
function resolveFormula(
  formulaId: string
): { formula: FormulaIdentity; herbIds: string[] } | null {
  const formula = allFormulaData.find((f) => f.id === formulaId);
  if (!formula) return null;
  return {
    formula: {
      id: formula.id,
      pinyin: formula.pinyin,
      english: formula.english,
      source: "nccaom",
      trustTier: "gold",
      resolutionRate: 1.0,
      herb_ids: formula.herb_ids,
    },
    herbIds: formula.herb_ids,
  };
}

/**
 * Looks up a single herb-drug pair in the interaction database.
 * Returns null when no interaction record exists — "no known interaction", not an error.
 */
function lookupInteraction(herbId: string, rxcui: string): InteractionMatch | null {
  const record = interactionData.interactions.find(
    (i) => i.herb.id === herbId && i.drug.rxcui === rxcui
  );
  if (!record) return null;

  const libraryHerb = lookupHerbById(herbId);

  const herb: HerbIdentity = {
    id: record.herb.id,
    pinyin: record.herb.pinyin,
    latin: record.herb.latin,
    english: record.herb.english,
    nccaom_code: record.herb.nccaom_code,
    trustTier: "gold",
    source: "nccaom_verified",
    resolved: true,
    active_constituent: libraryHerb?.active_constituent,
    taste: libraryHerb?.taste,
    temperature: libraryHerb?.temperature,
    channels: libraryHerb?.channels,
    properties: libraryHerb?.properties,
    tcm_cautions: libraryHerb?.tcm_cautions,
  };

  return {
    interactionId: record.id,
    herb,
    drug: record.drug as DrugIdentity,
    severity: record.severity as SeverityLevel,
    severityColor: record.severity_color as "red" | "yellow" | "green",
    mechanism: record.mechanism,
    clinicalSummary: record.clinical_summary,
    evidenceLevel: record.evidence_level as EvidenceLevel,
    evidenceNotes: record.evidence_notes,
    citations: record.citations as Citation[],
    sources: record.sources as DataSource[],
    confidence: record.confidence as "high" | "medium" | "low",
    fromFormula: false, // caller sets this if needed
  };
}

/**
 * Looks up full herb identity from Gold library first, Silver enriched fallback.
 * For "unresolved-{id}" herb IDs, returns a minimal HerbIdentity with trustTier: "unresolved".
 */
function lookupHerbById(herbId: string): HerbIdentity | null {
  // Unresolved herbs — id gap, cannot look up
  if (herbId.startsWith("unresolved-")) {
    return {
      id: herbId,
      pinyin: "Unknown Herb",
      latin: "",
      english: "Unresolved Herb",
      nccaom_code: null,
      trustTier: "unresolved",
      source: "unresolved",
      resolved: false,
    };
  }

  // Gold library
  const goldEntry = herbLibraryData.herbs.find((h) => h.id === herbId);
  if (goldEntry) {
    type GoldWithEnergetics = typeof goldEntry & {
      active_constituent?: string;
      taste?: string[];
      temperature?: string;
      channels?: string[];
      properties?: string[];
      tcm_cautions?: string;
    };
    const e = goldEntry as GoldWithEnergetics;
    return {
      id: e.id,
      pinyin: e.pinyin,
      latin: e.latin,
      english: e.english,
      nccaom_code: e.nccaom_code,
      trustTier: "gold",
      source: "nccaom_verified",
      resolved: true,
      active_constituent: e.active_constituent,
      taste: e.taste,
      temperature: e.temperature,
      channels: e.channels,
      properties: e.properties,
      tcm_cautions: e.tcm_cautions,
    };
  }

  // Silver enriched fallback
  const silverEntry = enrichedHerbs.find((h) => h.id === herbId);
  if (silverEntry) {
    return {
      id: silverEntry.id,
      pinyin: silverEntry.pinyin,
      latin: silverEntry.latin,
      english: silverEntry.english,
      nccaom_code: silverEntry.nccaom_code,
      trustTier: "silver",
      source: "tcmbank_research",
      tcmbank_id: silverEntry.tcmbank_id,
      resolved: silverEntry.resolved,
      active_constituent: silverEntry.active_constituent,
      taste: silverEntry.taste,
      temperature: silverEntry.temperature,
      channels: silverEntry.channels,
      properties: silverEntry.properties,
      tcm_cautions: silverEntry.tcm_cautions,
    };
  }

  return null;
}

function escalateSeverity(a: SeverityLevel, b: SeverityLevel): SeverityLevel {
  return SEVERITY_RANK[a] >= SEVERITY_RANK[b] ? a : b;
}

function severityToColor(severity: SeverityLevel): "red" | "yellow" | "green" {
  switch (severity) {
    case "contraindicated": return "red";
    case "precaution":      return "yellow";
    case "none":            return "green";
  }
}

